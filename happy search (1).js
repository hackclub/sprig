afterInput(() => {

  const p = getFirst(player)
  const portales = tilesWith(portal, player)
  
  if (portales.length > 0) {
    const destino = getFirst(portal2)
    p.x = destino.x
    p.y = destino.y
  }

  
  const faces = getAll(happyFace)
  
  for (let i = 0; i < faces.length; i++) {
    for (let j = i + 1; j < faces.length; j++) {
      const a = faces[i]
      const b = faces[j]
      const dx = Math.abs(a.x - b.x)
      const dy = Math.abs(a.y - b.y)
      
      if ((dx === 1 && dy === 0) || (dx === 0 && dy === 1)) {
        level++
        if (level < levels.length) {
          setMap(levels[level])
        } else {
          addText("Amazing job! You won! ", { y: 4, color: color`4` })
        }
      }
    }
  }

  
})