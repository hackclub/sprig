let started = 0;
onInput("w", () => {
  if (started == 0) {
    started = 1;
    start();
    clearText();
  } else if (started == -1) {
    clearText();
    started = 0;
    setMap(levels[0]);
    addText("Press W to start", { 
      x: 2,
      y: 6,
    });
  }
  jump();
});

function start() {
  let moveWalls, addWalls;
  let score = 0;
  moveWalls = setInterval(function() {
    clearText();
    score += 1;
    getAll(wallTop).forEach(function(e) {
      e.x -= 1;
      clearTile(0, 8);
    });
    getAll(wallBottom).forEach(function(e) {
      e.x -= 1;
      clearTile(0, 8);
    });
    if (getTile(3, 8).length > 1 || getTile(3, 7).length > 1) {
      started = -1;
      clearInterval(moveWalls);
      clearInterval(addWalls);
      clearTile(3, 8);
      clearTile(3, 7);
      addSprite(3, 8, dead);
      addText("womp womp", { 
        x: 0,
        y: 6,
      });
    }
    addText("" + score, { 
      x: 1,
      y: 5,
    });
  }, 100);
  
  addWalls = setInterval(function() {
    setTimeout(function() {
      let wall = Math.floor(Math.random() * (4 - 1) + 1);
      if (wall == 1 || wall == 2) {
        addSprite(29, 8, wallTop);
      } else {
        addSprite(29, 8, wallBottom);
        addSprite(29, 7, wallTop);
      }
    }, Math.floor(Math.random() * (700 - 1) + 1));
  }, 1200);
}

function jump() {
  getFirst(player).y = 7;
  setTimeout(function() {
    getFirst(player).y = 6;
  }, 100);
  setTimeout(function() {
    getFirst(player).y = 7;
  }, 300);
  setTimeout(function() {
    getFirst(player).y = 8;
  }, 400);
}

