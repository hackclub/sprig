setSolids(["p", "r"]);
let level = ``;
for(var i = 0; i < mapHeight; i++){
  for(var j = 0; j < mapWidth; j++){
    if(i === 0 || i === mapHeight-1 || j === 0 || j === mapWidth-1){
      level += "r";
    }else{
      level += "q";
    }
  }
  level += "\n";
}
setMap(level);
addSprite(1, 1, "p");
let current = "a";
let confirmDel = false;
let hideDel = false;
function doBorder(){
  var border = tilesWith("r");
  for(var i = 0; i < border.length; i++){
    var currentB = border[i];
    var found = false;
    for(var j = 0; j < currentB.length; j++){
      if(colors.includes(currentB[j].type)){
        currentB[j].type = current;
        found = true;
      }
    }
    if(!found){
      addSprite(currentB[0].x, currentB[0].y, "a");
    }
  }
}
function mod(n, m) {
  return (n<0)?(m+n):((n>=m)?(n-m):(n));
}
onInput("w", () => {
  getFirst("p").y -= 1
});
onInput("a", () => {
  getFirst("p").x -= 1
});
onInput("s", () => {
  getFirst("p").y += 1
});
onInput("d", () => {
  getFirst("p").x += 1
});
onInput("i", () => {
  current = colors[mod(colors.indexOf(current)+1, colors.length)];
});
onInput("k", () => {
  current = colors[mod(colors.indexOf(current)-1, colors.length)];
});
onInput("j", () => {
  var tile = getTile(getFirst("p").x, getFirst("p").y);
  for(var i = 0; i < tile.length; i++){
    if(colors.includes(tile[i].type) || tile[i].type === "q"){
      tile[i].type = current;
    }
  }
});
onInput("l", () => {
  if(!confirmDel){
    addText("Are you sure you\nwant to delete\nyour work? Press L\nagain to confirm.\nOtherwise, press\nany other button", {x: 1, y:1});
    confirmDel = true;
  }else{
    setMap(level);
    doBorder();
    addSprite(1, 1, "p");
    clearText();
  }
});
doBorder();
afterInput(() => {
  doBorder();
  if(hideDel){
    clearText();
    confirmDel = false;
  }
  if(confirmDel){
    hideDel = true;
  }else{
    hideDel = false;
  }
});
