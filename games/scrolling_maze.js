const map_dimensions = [9, 7];
const map_center = [4, 3];

const xy_pos = [1, 2];

onInput("a", () => {
  if(!checkCollision("a")){
    xy_pos[0] += 1;
  }
});
onInput("d", () => {
  if(!checkCollision("d")){
    xy_pos[0] -= 1;
  }
});
onInput("w", () => {
  if(!checkCollision("w")){
    xy_pos[1] += 1;
  }
});
onInput("s", () => {
  if(!checkCollision("s")){
    xy_pos[1] -= 1;
  }
});

