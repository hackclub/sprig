const colors = ["red", "blue", "yellow", "green"]

const greenTune = tune`
500: d5^500,
15500`
const redTune = tune`
500: c5^500,
15500`
const blueTune = tune`
500: b4^500,
15500`
const yellowTune = tune`
500: a4^500,
15500`

const setColor = (val) => {
  switch (val) {
    case "red":
      playTune(redTune)
      break
    case "blue":
      playTune(blueTune)
      break
    case "yellow":
      playTune(yellowTune)
      break
    case "green":
      playTune(greenTune)
      break
  }
  setMap(maps[val])
}

let sequence = []
let current = 0
let gameOver = false
let animating = false

const hitEnd = () => {
  current = 0
  sequence.push(colors[Math.floor(Math.random()*colors.length)])
  animating = true
  sequenceAnimation(0)
}

const sequenceAnimation = (i) => {
  if (i == sequence.length) return animating = false

  setColor(sequence[i])
  setTimeout(() => {
    setColor("default")
    setTimeout(() => sequenceAnimation(i + 1), 200)
  }, 500)
}

const check = val => {

  if (gameOver || animating) return
  
  if (sequence[current] == val) {
    animating = true
    setColor(val)
      setTimeout(() => {
        setColor("default")
        animating = false
      }, 200)
    current++
    if (current == sequence.length) setTimeout(() => {
        hitEnd()
      }, 500)
  } else {
    setColor("blank")
    addText("Game Over!", { y: 4 })
    addText(`Score: ${sequence.length - 1}`, {y: 6})
    gameOver = true
  }
}

onInput("w", () => {
  check("green")
});

onInput("a", () => {
  check("yellow")
});

onInput("s", () => {
  check("blue")
});

onInput("d", () => {
  check("red")
});

hitEnd()
