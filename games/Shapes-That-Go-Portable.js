function scrollLevel(player) {
  const obstacles = [
    ...getAll(squareChar),
    ...getAll(spikeChar),
    ...getAll(upsideDownSpikeChar)
  ]
  const bounces = getAll(bounceChar)
  const pogs = getAll(pogChar);

  // Handle obstacles
  for (const obstacle of obstacles) {
    const nextXPosition = obstacle.x - 1;

    // Handle collision
    // If the next position of the obstacle is the player, then end the game
    if (nextXPosition == player.x && obstacle.y == player.y) {
      endGame()
    } else {
      if (obstacle.x == 0) {
        clearTile(0, obstacle.y);
      }
    }

    obstacle.x = nextXPosition;
  }

  // Handle pogs
  for (const pog of pogs) {
    const nextXPosition = pog.x - 1;

    // Handle collision
    if (nextXPosition == player.x && pog.y == player.y) {
      score += 1000
      clearTile(pog.x, pog.y)
    } else {
      if (pog.x == 0) {
        clearTile(0, pog.y)
      }
    }
    pog.x = nextXPosition
  }

  for (const bounce of bounces) {
    const nextXPosition = bounce.x - 1;

    // Handle collision
    let isAffected = (gravityFlipped ? bounce.y + 1 : bounce.y - 1) == player.y;
    if (nextXPosition == player.x && isAffected) {
      playerYVelocity = 6
    } else {
      if (bounce.x == 0) {
        clearTile(0, bounce.y)
      }
    }

    bounce.x = nextXPosition
  }
}

function getRandomInt(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getRandomChunk() {
  const randomNumber = getRandomInt(0, chunks.length);
  return chunks[randomNumber];
}

let chunkPassed = true
let currentChunk = null
let streamedChunkX = 0;
let streamedChunkY = 0;

function loadChunk(chunk, isPartialStream) {
  if (!chunk) {
    return;
  }
  
  if (chunk[0].length > levelX) {
    // Stream the chunk as it overflows on the x-axis
    chunkPassed = false;

    if (isPartialStream) {
      // Load as much as possible from left to right
      for (let chunkY = 0; chunkY < chunk.length; chunkY++) {
        if (chunkY > levelY) {
          continue;
        }
        
        for (let chunkX = 0; chunkX < Math.min(levelX, chunk[0].length); chunkX++) {
          if (chunk[chunkY][chunkX] == 1) {
            addSprite(chunkX, chunkY, squareChar);
          }
        }
      }
    } else {
      // Normal streaming logic
      for (let chunkY = 0; chunkY < chunk.length; chunkY++) {
        if (chunkY > levelY) {
          continue;
        }
        
        if (!gameEnded) {
          switch (chunk[chunkY][streamedChunkX]) {
            case 1: {
              addSprite(levelX - 1, chunkY, squareChar);
              break;
            }
            case 2: {
              addSprite(levelX - 1, chunkY, spikeChar);
              break;
            }
            case 3: {
              addSprite(levelX - 1, chunkY, upsideDownSpikeChar);
              break;
            }
            case 4: {
              addSprite(levelX - 1, chunkY, bounceChar);
              break;
            }
            case 7: {
              addSprite(levelX - 1, chunkY, pogChar);
              break;
            }
          }
          
        }
      }

      if (streamedChunkX == chunk[0].length - 1) {
        streamedChunkX = 0;
        chunkPassed = true;
      } else {
        streamedChunkX += 1;
      }
    }
  } else {
    // Level does not overflow, we're safe to load the full thing
    for (let y = 0; y < chunk.length; y++) {
      for (let x = 0; x < chunk[y].length; x++) {
        // Check if the value is non-zero and within the x limit
        if (chunk[y][x] == 1 && x < levelX) {
          addSprite(x, y, squareChar);
        }
      }
    }
  }
}

function isPlayerOnGround(player, isGravityFlipped) {
  const yPosToCheck = isGravityFlipped ? player.y - 1 : player.y + 1;
  const tiles = getTile(player.x, yPosToCheck);
  if (tiles != "") {
    switch (tiles[0]._type) {
      case "s": {
        return true;
      }
      case "b": {
        return true;
      }
      case "i": {
        return false;
      }
      case "u": {
        return false;
      }
    }
  }
  return false;
}

