/// Game loop
function beginGameLoop() {
  // Load map
  setMap(levels.game)
  
  // Get the player then set some values
  player = getFirst(playerChar);
  isJumping = false;
  gravityFlipped = false;
  score = 0;
  jumpingStage = 0;
  playerYVelocity = 0;
  gameStarted = true;
  gameEnded = false;

  // Reset chunk data
  chunkPassed = true
  currentChunk = null
  streamedChunkX = 0;
  streamedChunkY = 0;

  // Load starting level
  loadChunk(startingChunk, true)
  currentChunk = startingChunk
  
  const gameInterval = setInterval(() => {
    if (player) {
      // Apply gravity or jumping logic
      if (!isPlayerOnGround(player, gravityFlipped) && !isJumping) {
        // Gravity pulls the player down
        player.y += gravityFlipped ? -1 : 1;
      } else if (isJumping) {
        // Jumping logic with stages for smoother ascent and descent
        if (jumpingStage == 0) {
          player.y -= gravityFlipped ? -2 : 2; // Strong initial jump
        } else if (jumpingStage == 1) {
          player.y -= gravityFlipped ? -1 : 1; // Mid-air slowdown
        } else if (jumpingStage == 2) {
          player.y -= gravityFlipped ? 0 : 0; // Pause briefly at the top
        }
        
        jumpingStage += 1;
        
        // End jump after 3 stages
        if (jumpingStage >= 3) {
          isJumping = false;
          jumpingStage = 0;
        }
      }

      if (player.y == levelY || player.y == 0) {
        endGame()
      }
    }

    // Handle player y velocity
    if (playerYVelocity > 0) {
      player.y -= gravityFlipped ? -2 : 2;
      playerYVelocity -= playerYVelocity >= 2 ? 2 : 1;
    }
  
    if (gameStarted) {
       // Load next chunk
      const chunk = chunkPassed ? getRandomChunk() : currentChunk;
      if (chunkPassed) {
        currentChunk = chunk
      }
      
      scrollLevel(player)
      loadChunk(chunk)
      
      // Add to the player's score
      score += 100
      setScore(score)
    }

    if (gameEnded) {
      // End the game loop
      clearInterval(gameInterval);

      // Game over screen text
      addText("Game Over", {
        x: 5,
        y: 6,
        color: color`0`
      });

      addText("Up to reset", {
        x: 4,
        y: 8,
        color: color`0`
      });
    }
  }, 65)

  return gameInterval
}

// Create the game loop
let gameInterval = beginGameLoop()

// Handle game end
function endGame() {
  gameEnded = true;
  gameStarted = false;
  setMap(levels.gameOver);
}

// Handle game restarting
function restartGame() {
  gameInterval = beginGameLoop()
}

