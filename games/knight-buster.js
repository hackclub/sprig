onInput("w", () => {
  getFirst(p1).y -= 1
  whoMoved = 1
})
onInput("a", () => {
  getFirst(p1).x -= 1
  whoMoved = 1
})
onInput("s", () => {
  getFirst(p1).y += 1
  whoMoved = 1
})
onInput("d", () => {
  getFirst(p1).x += 1
  whoMoved = 1
})
onInput("i", () => {
  getFirst(p2).y -= 1
  whoMoved = 2
})
onInput("j", () => {
  getFirst(p2).x -= 1
  whoMoved = 2
})
onInput("k", () => {
  getFirst(p2).y += 1
  whoMoved = 2
})
onInput("l", () => {
  getFirst(p2).x += 1
  whoMoved = 2
})

afterInput(() => {
  if (level === 1) {
    playTune(move)
    if (whoMoved === 2) {
      if (tilesWith(p2, box).length === 1) {
        p2_lives -= 1
        playTune(p2_sound)
      }
    }
    if (tilesWith(p1, p2).length === 1) {
      p1_lives -= 1
      playTune(p1_sound)
    }
    if (winner === 0) {
      if (p1_lives === 0) {
        winner = 2
      } else if (p2_lives === 0) {
        winner = 1
      }
    } else if (winner === 1) {
      addText("Knight Wins!", { y: 4, color: color`5` });
    } else if (winner === 2) {
      addText("Ghost Wins!", { y: 4, color: color`3` });
    }
  } else {
    const allSprites = getAll()
    allSprites.forEach(sprite => {
      sprite.remove()
    })
    introMusic.end()
    clearText()
    level += 1
    playTune(intro, Infinity)
    playTune(bgm, Infinity)
    for (let i = 1; i < 16; i++) {
      addSprite(Math.floor(Math.random() * 10), Math.floor(Math.random() * 8), box)
    }
    player1 = addSprite(0,0,p1)
    player2 = addSprite(9, 7, p2)
  }
});