function initializeGame() {
  alive = true
  clearText()

  setMap(baseMap)
  setBackground(background)

  for (let row = 0; row < grids[currLevel].length; row++) {
    for (let col = 0; col < grids[currLevel][row].length; col++) {
      if (grids[currLevel][row][col] == 1) {
        summonSpike(col * 8, row * 8)
      } else if (grids[currLevel][row][col] == 2) {
        summonWall(col * 8, row * 8)
      } else if (grids[currLevel][row][col] == 3) {
        summonPlayer(col * 8, row * 8)
      } else if (grids[currLevel][row][col] == 4) {
        summonGoal(col * 8, row * 8)
      }
    }
  }

  alive = true


  addText("Level " + (currLevel + 1), { x: 6, y: 7, color: color`0` })
  addText("Deaths: " + deaths, { x: 6, y: 8, color: color`0` })
  addText("Shields: " + shields, { x: 6, y: 9, color: color`0` })

  setTimeout(() => {
    clearText()
  }, 1500)
}

onInput("w", () => {
  if (alive == true) {
    let playerPixels = getAll(playerPixel)

    if (isFrontEmpty(0, -1, playerPixels)) {
      for (let i = 0; i < playerPixels.length; i++) {
        playerPixels[i].y -= 1
      }
    }
  }
})

onInput("a", () => {
  if (alive == true) {
    let playerPixels = getAll(playerPixel)

    if (isFrontEmpty(-1, 0, playerPixels)) {
      for (let i = 0; i < playerPixels.length; i++) {
        playerPixels[i].x -= 1
      }
    }
  }
})

onInput("s", () => {
  if (alive == true) {
    let playerPixels = getAll(playerPixel)

    if (isFrontEmpty(0, 1, playerPixels)) {
      for (let i = 0; i < playerPixels.length; i++) {
        playerPixels[i].y += 1
      }
    }
  }
})

onInput("d", () => {
  if (alive == true) {
    let playerPixels = getAll(playerPixel)

    if (isFrontEmpty(1, 0, playerPixels)) {
      for (let i = 0; i < playerPixels.length; i++) {
        playerPixels[i].x += 1
      }
    }
  }
})

onInput("i", () => {
  if (alive == false) {
    initializeGame()
  }
})

afterInput(() => {
  let playerPixels = getAll(playerPixel)
  let spikePixels = getAll(spikePixel)
  let goalPixels = getAll(goalPixel)

  for (p of playerPixels) {
    for (s of spikePixels) {
      if (p.x == s.x && p.y == s.y && alive) {
        deaths++
        if (shields > 0) {
          shields--
        }

        alive = false

        addText("You Died!", { x: 1, y: 6, color: color`0` })
        addText("Deaths: " + deaths, { x: 1, y: 7, color: color`0` })
        addText("Shields: " + shields, { x: 1, y: 8, color: color`0` })
        addText("Press i to respawn", { x: 1, y: 9, color: color`0` })
      }
    }

    for (g of goalPixels) {
      if (p.x == g.x && p.y == g.y && currLevel < grids.length - 1 && alive) {
        currLevel++
        shields++
        alive = false
        initializeGame()
      }
    }
  }
})

let alive = true
let currLevel = 5
let deaths = 0
let shields = 0

initializeGame()
