const smallestPossibleXValue = 0
const highestPossibleXValue = 159
const smallestPossibleYValue = 0
const highestPossibleYValue = 127

const playerSize = 6

function summonSpike(x, y) {
  for (let i = 0; i < 8; i++) {
    addSprite(x + i, y + i, spikePixel)
    addSprite(x + 7 - i, y + i, spikePixel)
  }
}

function summonWall(x, y) {
  for (let i = 0; i < 8; i++) {
    for (let j = 0; j < 8; j++) {
      addSprite(x + i, y + j, wallPixel)
    }
  }
}

function summonGoal(x, y) {
  for (let i = 0; i < 8; i++) {
    for (let j = 0; j < 8; j++) {
      addSprite(x + i, y + j, goalPixel)
    }
  }
}

function summonPlayer(x, y) {
  for (let i = 7 - playerSize; i <= playerSize; i++) {
    for (let j = 7 - playerSize; j <= playerSize; j++) {
      addSprite(x + i, y + j, playerPixel)
    }
  }
}

function isFrontEmpty(x, y, playerPixels) {

  for (let i = 0; i < playerPixels.length; i++) {
    let newX = playerPixels[i].x + x
    let newY = playerPixels[i].y + y

    //Check if player is in screen boundaries
    if (newX < smallestPossibleXValue ||
      newX > highestPossibleXValue ||
      newY < smallestPossibleYValue ||
      newY > highestPossibleYValue
    ) {
      return false
    }

    let tileContents = getTile(newX, newY)
    //Check for walls
    for (let sprite of tileContents) {
      if (sprite.type == wallPixel) {
        return false
      }
    }

    //Check for Spikes
    for (let sprite of tileContents) {
      if (sprite.type == spikePixel && shields > 0) {
        shields--
        return false
      }
    }
  }

  return true
}

