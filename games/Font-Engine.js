const textRenderer = new ScrollableTextRenderer()

// Input handling for scrolling with cooldown
onInput("s", () => {
  textRenderer.scrollDown()
})

onInput("w", () => {
  textRenderer.scrollUp()
})

onInput("i", () => {
  textRenderer.jumpToTop()
})

onInput("k", () => {
  textRenderer.jumpToBottom()
})

