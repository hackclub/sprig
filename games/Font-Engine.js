// ==================== COMPLEX SCROLLING TEXT ENGINE ====================

class ScrollableTextRenderer {
  constructor() {
    this.scrollY = 0 // Current scroll offset in tiles
    this.contentHeight = 0 // Total height of rendered content
    this.buffer = [] // 2D array of characters [y][x]
    this.maxScroll = 0
    this.lastScrollTime = 0
    this.uiSprites = [] // Track UI indicator sprites
  }
  
  /**
   * Word wrap with hyphenation support for very long words
   * @param {string} text - Text to wrap
   * @param {number} maxWidth - Maximum line width (default: SCREEN_WIDTH)
   * @returns {string[][]} Array of word arrays for each line
   */
  wrapText(text, maxWidth = SCREEN_WIDTH) {
    if (!text || text.trim().length === 0) {
      return []
    }
    
    const words = text.split(" ")
    const lines = []
    let currentLine = []
    let currentLength = 0
    
    for (let word of words) {
      // Handle explicit newlines in word
      if (word.includes("\n")) {
        const parts = word.split("\n")
        for (let i = 0; i < parts.length; i++) {
          if (i > 0) {
            lines.push(currentLine)
            currentLine = []
            currentLength = 0
          }
          if (parts[i].length > 0) {
            const wordLen = parts[i].length
            if (currentLength + wordLen + (currentLine.length > 0 ? 1 : 0) <= maxWidth) {
              currentLine.push(parts[i])
              currentLength += wordLen + (currentLine.length > 1 ? 1 : 0)
            } else {
              if (currentLine.length > 0) lines.push(currentLine)
              currentLine = [parts[i]]
              currentLength = wordLen
            }
          }
        }
        continue
      }
      
      const wordLen = word.length
      
      // If word is longer than screen width, hyphenate it
      if (wordLen > maxWidth) {
        if (currentLine.length > 0) {
          lines.push(currentLine)
          currentLine = []
          currentLength = 0
        }
        
        let remaining = word
        while (remaining.length > maxWidth) {
          // Find good hyphenation point (preferably at syllable boundary or just max-1)
          const splitPoint = maxWidth - 1
          lines.push([remaining.substring(0, splitPoint) + "-"])
          remaining = remaining.substring(splitPoint)
        }
        currentLine = [remaining]
        currentLength = remaining.length
      } else {
        // Check if word fits on current line
        const spaceNeeded = (currentLine.length > 0 ? 1 : 0) + wordLen
        
        if (currentLength + spaceNeeded <= maxWidth) {
          currentLine.push(word)
          currentLength += spaceNeeded
        } else {
          // Word doesn't fit, wrap to next line
          lines.push(currentLine)
          currentLine = [word]
          currentLength = wordLen
        }
      }
    }
    
    if (currentLine.length > 0) {
      lines.push(currentLine)
    }
    
    return lines
  }
  
  /**
   * Convert wrapped lines to 2D character buffer
   * @param {string[][]} lines - Array of word arrays
   */
  buildBuffer(lines) {
    this.buffer = []
    for (let line of lines) {
      const lineStr = line.join(" ")
      const row = lineStr.split("").map(c => c.toLowerCase())
      // Pad to screen width
      while (row.length < SCREEN_WIDTH) {
        row.push(null) // null represents empty space
      }
      this.buffer.push(row)
    }
    this.contentHeight = this.buffer.length
    this.maxScroll = Math.max(0, this.contentHeight - SCREEN_HEIGHT)
  }
  
  /**
   * Clear UI sprites from previous render
   */
  clearUI() {
    for (const sprite of this.uiSprites) {
      if (sprite) sprite.remove()
    }
    this.uiSprites = []
  }
  
  /**
   * Render visible portion based on scrollY
   */
  render() {
    // Clear screen
    setMap(map`
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................
....................`)
    
    // Clear old UI sprites
    this.clearUI()
    
    // Render visible lines
    for (let screenY = 0; screenY < SCREEN_HEIGHT; screenY++) {
      const bufferY = screenY + this.scrollY
      if (bufferY >= this.buffer.length) break
      
      const row = this.buffer[bufferY]
      if (!row) continue
      
      for (let x = 0; x < SCREEN_WIDTH; x++) {
        const char = row[x]
        if (char && char !== " ") {
          addSprite(x, screenY, char)
        }
      }
    }
    
    // Draw scroll indicators if needed
    this.drawScrollIndicators()
    
    // Draw progress indicator
    this.drawProgressIndicator()
  }
  
  /**
   * Draw up/down arrows if content is scrollable
   */
  drawScrollIndicators() {
    if (this.maxScroll <= 0) return
    
    // Up arrow (if not at top) - position at top-right
    if (this.scrollY > 0) {
      this.uiSprites.push(addSprite(SCREEN_WIDTH - 1, 0, "▲"))
    }
    
    // Down arrow (if not at bottom) - position at bottom-right
    if (this.scrollY < this.maxScroll) {
      this.uiSprites.push(addSprite(SCREEN_WIDTH - 1, SCREEN_HEIGHT - 1, "▼"))
    }
  }
  
  /**
   * Draw scroll progress indicator
   */
  drawProgressIndicator() {
    if (this.maxScroll <= 0) return
    
    // Calculate position (0 to SCREEN_HEIGHT - 3, leaving margin)
    const availableHeight = SCREEN_HEIGHT - 2
    const progress = this.scrollY / this.maxScroll
    const indicatorY = 1 + Math.round(progress * (availableHeight - 1))
    
    this.uiSprites.push(addSprite(SCREEN_WIDTH - 1, indicatorY, "●"))
  }
  
  /**
   * Check if scroll action is allowed (cooldown)
   * @returns {boolean} True if can scroll
   */
  canScroll() {
    const now = Date.now()
    if (now - this.lastScrollTime < SCROLL_COOLDOWN) {
      return false
    }
    this.lastScrollTime = now
    return true
  }
  
  /**
   * Scroll up by amount
   * @param {number} amount - Lines to scroll
   */
  scrollUp(amount = SCROLL_SPEED) {
    if (!this.canScroll()) return
    this.scrollY = Math.max(0, this.scrollY - amount)
    this.render()
  }
  
  /**
   * Scroll down by amount
   * @param {number} amount - Lines to scroll
   */
  scrollDown(amount = SCROLL_SPEED) {
    if (!this.canScroll()) return
    this.scrollY = Math.min(this.maxScroll, this.scrollY + amount)
    this.render()
  }
  
  /**
   * Jump to top of content
   */
  jumpToTop() {
    if (!this.canScroll()) return
    this.scrollY = 0
    this.render()
  }
  
  /**
   * Jump to bottom of content
   */
  jumpToBottom() {
    if (!this.canScroll()) return
    this.scrollY = this.maxScroll
    this.render()
  }
  
  /**
   * Process and display text
   * @param {string} text - Text to display
   */
  display(text) {
    if (!text || typeof text !== "string") {
      console.error("Invalid text provided to display()")
      return
    }
    
    const lines = this.wrapText(text)
    if (lines.length === 0) {
      console.warn("No content to display")
      return
    }
    
    this.buildBuffer(lines)
    this.scrollY = 0
    this.lastScrollTime = 0
    this.render()
  }
  
  /**
   * Get current scroll percentage (0-100)
   * @returns {number} Scroll percentage
   */
  getScrollPercent() {
    if (this.maxScroll <= 0) return 100
    return Math.round((this.scrollY / this.maxScroll) * 100)
  }
}

