function randomCatMove() {
  const x = Math.floor(Math.random() * 16)
  const y = Math.floor(Math.random() * 16)
  clearTile(catPos[0], catPos[1])
  addSprite(x, y, cat)
  catPos = [x, y]
}

onInput("w", () => {
  getFirst(player).y -= 1
})
onInput("s", () => {
  getFirst(player).y += 1
})
onInput("a", () => {
  getFirst(player).x -= 1
})
onInput("d", () => {
  getFirst(player).x += 1
})

afterInput(() => {
  const p = getFirst(player)
  const c = getFirst(cat)
  if (p.x === c.x && p.y === c.y) {
    addText("You caught the cat!", {
      x: 4,
      y: 7,
      color: color`3`
    })
    setTimeout(() => {
      clearText()
      clearTile(getFirst(player).x, getFirst(player).y)
      clearTile(catPos[0], catPos[1])
      setMap(level)
      addSprite(playerStart[0], playerStart[1], player)
      addSprite(catStart[0], catStart[1], cat)
      catPos = [...catStart]
    }, 2000)
  }
})
setInterval(() => {
  randomCatMove()
}, 3000)
