function spawnCar(){
  var posY = Math.floor(Math.random()*5)
  var speed = Math.floor(Math.random()*2) + 1

  if (Math.floor(Math.random()*20) == 3){
    addSprite(9, posY, "m");
  } else {
    addSprite(9, posY, "c");
  }
  Object.defineProperty(getTile(9, posY)[0], "speed", {value: speed});
  console.log(speed)
}

function start(){
 var frame = setInterval(function(){

   //lose
  if(getAll("p").length == 0){
    playback.end()
    clearText();
    setMap(levels[3])
    playTune(lose)
    playTune(womp, 5)
    clearInterval(frame)
    addText("GAME OVER!", {
      x: 3,
      y: 3,
      color: color`2`
    });
    addText("Score: " + (wave*10), {
      x: 3,
      y: 4,
      color: color`2`
    });
    addText("womp womp ", {
      x: 3,
      y: 6,
      color: color`2`
    });
    return;
  }

  //win!
  if(wave == 100){
    clearText();
    playback.end()
    setMap(levels[2])
    clearInterval(frame)
    playTune(win)
    addText("YOU WIN!", {
      x: 2,
      y: 3,
      color: color`2`
    })
    addText("Score: " + (wave*10), {
      x: 3,
      y: 4,
      color: color`2`
    });
    return;
  }
  
  getAll("b").forEach((cars) => {
    clearTile(cars.x, cars.y)
  })

  getAll("n").forEach((xMilks) => {
    xMilks.remove()
  })
  
  let carChance = Math.floor(Math.random()*10);
  if (carChance <= (carProb-(1*(diff/1000)))){
    wave += 1
    spawnCar();
  }

  

  getAll("c").concat(getAll("m")).forEach((cars) => {
    //ensure cars dont clip out
    if(cars.x - cars.speed < 0){
      cars.x = 0;
    } else {
      cars.x -= cars.speed
    }

    //car collision logic
    if(cars.x == 0){
      let addX = cars.x
      let addY = cars.y
      if(cars.type == "m"){
        getTile(cars.x, cars.y).forEach((entity) => {
          if(entity.type == "p"){
            getAll("c").forEach((remove) => {
              addSprite(remove.x, remove.y, "b")
              remove.remove();
            });
          }
        })
        cars.remove()
        playTune(mCrash)
        addSprite(addX, addY, "n");
      } else {
        clearTile(cars.x, cars.y)
        playTune(crash)
        addSprite(addX, addY, "b");
      }
    }
  })

  if (diff>200){
    diff -= 10
    addText("Spawn Spd: " + diff, {
      x: 0,
      y: 0,
      color: color`2`
    })
  }
  console.log(diff)
  addText("Score: " + (wave*10), {
    x: 0,
    y: 1,
    color: color`2`
  });

  }, diff); 
}

