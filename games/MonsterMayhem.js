function generateScreen(SCREEN_ART) {
	const splitArt = splitBitmap(SCREEN_ART);
	const legend = splitArt.map(chunk => [
		getLegendChar2(),
		chunk,
	]);

	// create the map by joining every char for the image into one string, then inserting newlines
	const map = legend
		.map((v) => v[0])
		.join("")
		.replace(/(.{10})/g, "$1\n")
		.slice(0, -1);

	return [legend, map];
}

