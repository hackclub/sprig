function circle(pos, radius) {
  pos = new Vector(pos.x / 2, pos.y)
  pos = pos.toInt()
  for (let x = 0; x < radius * 2; x++) {
    drawPixel(x + pos.x, 128 - Math.floor(Math.sqrt(radius - Math.pow(x - pos.x, 2)) + pos.y), help ? "4" : "5")
  }
  for (let x = 0; x < radius * 2; x++) {
    drawPixel(x + pos.x, 128 - Math.floor(-Math.sqrt(radius - Math.pow(x - pos.x, 2)) + pos.y), help ? "4" : "5")
  }
}


