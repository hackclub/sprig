class CircleCollider {
  constructor(pos, radius) {
    this.pos = pos
    this.radius = radius
  }

  isColliding(other) {
    let center = this.pos.plus(other.pos.times(-1))
    let xDist = Math.cos(center.getAngle()) * (this.radius + other.radius)
    return Math.abs(center.x) < Math.abs(xDist)
  }
}

